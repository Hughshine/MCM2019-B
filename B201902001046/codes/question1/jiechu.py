print('假设周期性运动，接触时间不计，球体再次向上运动时速度等于下落速度')
m_ball = 0.27
mv = m_ball * 2 * v  # 只有大小，方向向上
print('排球受到的冲量:', mv)
t_strike = 0.05  # 假设测量到撞击时间为0.05s
print('接触时间:', t_strike)
print('接触过程中的平均力:', mv / t_strike)

