from math import sqrt
'''
接触力、接触时间、接触做功
'''

E1 = 140 * 1e6  # 弹性模量假设，Pa
E2 = 140 * 1e6
v1 = 0.4  # 泊松比假设，无量纲
v2 = 0.4
R = 0.1  # 排球半径约为 0.1m
d = 0.005  # 凹陷深度，5mm

_E = 1 / ((1-v1**2)/E1 + (1-v2**2)/E2)

F = 4/3 * _E * sqrt(R) * (d ** 1.5)

print('_E=', _E)
print('受力大小=', F)

